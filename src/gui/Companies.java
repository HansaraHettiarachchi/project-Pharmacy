    private boolean validateField() {
        c_name = jTextField1.getText();
        s_name = String.valueOf(jComboBox1.getSelectedItem());
        
        if (s_name.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Supplier.", "Please check fields", JOptionPane.ERROR_MESSAGE);
        } else if (c_name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter company name.", "Please check fields", JOptionPane.ERROR_MESSAGE);
        } else if (!c_name.matches("^[a-zA-Z0-9() ]+$")) {
            JOptionPane.showMessageDialog(this, "Unnesasery charactors included in Company name.", "Please check fields", JOptionPane.ERROR_MESSAGE);
        } else if (c_name.length() > 70) {
            JOptionPane.showMessageDialog(this, "Company name should contains lower than 70 charactors.", "Please check fields", JOptionPane.ERROR_MESSAGE);
        } else {
            return true;
        }
        
        return false;
    }
    

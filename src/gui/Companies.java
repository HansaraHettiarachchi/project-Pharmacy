    private void setTableData(String rest, String tColumns[]) {
        DefaultTableModel tb = (DefaultTableModel) jTable1.getModel();
        
        String query = "SELECT * FROM `company` INNER JOIN `suppliers` ON  suppliers.id = company.suppliers_id "
                + "LEFT JOIN `address` ON address.id = company.address_id " + rest + ";";
        try {
            ResultSet rs = MySql.select(query);
            
            tb.setRowCount(0);
            
            while (rs.next()) {
                Vector<String> v = new Vector();
                
                for (String colunm : tColumns) {
                    v.add(rs.getString(colunm));
                }
                
                tb.addRow(v);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
    }
    

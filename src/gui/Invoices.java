    private String setTotal() {
        int rowCount = jTable1.getRowCount();
        double tot = 0;
        for (int i = 0; i < rowCount; i++) {
            String value = String.valueOf(jTable1.getValueAt(i, 4)).replace(" ", "");
            tot += Double.valueOf(value);
        }
        String data = Onload.df.format(tot) + " LKR";
        jLabel9.setText(data);
        return data;
    }

    private void onLoad() {
        setTableData("ORDER BY `DAT`");
        try {
            ResultSet rs = MySql.select("SELECT DISTINCT DATE_FORMAT(DAT, '%Y-%m') AS month FROM `invoice` ORDER BY `month` DESC;");
            Vector<String> months = new Vector<>();

            DateTimeFormatter f = DateTimeFormatter.ofPattern("MMMM, yyyy");
            months.add("SELECT");
            while (rs.next()) {
                YearMonth y = YearMonth.parse(rs.getString("month"));
                months.add(y.format(f));
            }
            DefaultComboBoxModel cb = new DefaultComboBoxModel(months);
            jComboBox2.setModel(cb);
            setTotal();
        } catch (Exception ex) {
            l.log(Level.WARNING, "While selecting months", ex);
        }

    }


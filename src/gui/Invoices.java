    private void setTableData(String rest) {
        try {
            ResultSet rs = MySql.select("SELECT * FROM `invoice` " + rest + "  ;");

            DefaultTableModel tbm = (DefaultTableModel) jTable1.getModel();
            tbm.setRowCount(0);
            int i = 0;

            while (rs.next()) {
                i++;
                Vector<String> v = new Vector<>();
                v.add(String.valueOf(i));
                v.add(rs.getString("id"));
                v.add(rs.getString("DAT"));
                v.add(Onload.df.format(Double.valueOf(rs.getString("discount"))));
                v.add(Onload.df.format(Double.valueOf(rs.getString("value"))));

                tbm.addRow(v);
            }

            setTotal();

        } catch (Exception ex) {
            l.log(Level.WARNING, "While selecting Invoice", ex);
        }
    }

